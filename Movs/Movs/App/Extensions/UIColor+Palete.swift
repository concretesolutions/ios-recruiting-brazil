import UIKit

extension UIColor {
  
  @nonobjc class var navy: UIColor {
    return UIColor(red: 43.0 / 255.0, green: 47.0 / 255.0, blue: 68.0 / 255.0, alpha: 1.0)
  }
  
  @nonobjc class var mango: UIColor {
    return UIColor(red: 246 / 255.0, green: 205.0 / 255.0, blue: 100.0 / 255.0, alpha: 1.0)
  }

}
